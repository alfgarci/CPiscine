int	ft_sqrt(int nb)
{
	int	i;
	int	b;

	b = nb;
	i = 0;
	if (b <= 0)
		return (0);
	if (b == 1)
		return (1);
	if (b >= 2)
	{
		while (i * i <= b)
		{
			if (i * i == b)
				return (i);
			else if (i >= 46341)
				return (0);
			i++;
		}
	}
	return (0);
}
